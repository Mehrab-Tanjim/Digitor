package LogicEngine;

public class Variable {
    private char name;
    private char truthValue;

    public char getName() {
        return name;
    }

    public void setName(char name) {
        this.name = name;
    }

    public char getTruthValue() {
        return truthValue;
    }

    public void setTruthValue(char truthValue) {
        this.truthValue = truthValue;
    }
}
